// Original Spanish cache
const esCache = {};

function initLanguage() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    esCache[el.getAttribute('data-i18n')] = el.innerHTML;
  });
  document.querySelectorAll('[data-i18n-concept]').forEach(el => {
    esCache[el.getAttribute('data-i18n-concept')] = el.innerHTML;
  });

  const savedLang = localStorage.getItem('goshenLang') || 'es';
  setLanguage(savedLang);

  document.getElementById('btn-es').addEventListener('click', () => setLanguage('es'));
  document.getElementById('btn-en').addEventListener('click', () => setLanguage('en'));
}

function setLanguage(lang) {
  localStorage.setItem('goshenLang', lang);

  document.getElementById('btn-es').classList.toggle('active', lang === 'es');
  document.getElementById('btn-en').classList.toggle('active', lang === 'en');

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (lang === 'en' && enTranslations[key]) {
      el.innerHTML = enTranslations[key];
    } else if (lang === 'es' && esCache[key]) {
      el.innerHTML = esCache[key];
    }
  });

  document.querySelectorAll('[data-i18n-concept]').forEach(el => {
    const key = el.getAttribute('data-i18n-concept');
    if (lang === 'en' && conceptTranslations[key]) {
      el.innerHTML = conceptTranslations[key];
    } else if (lang === 'es' && esCache[key]) {
      el.innerHTML = esCache[key];
    }
  });

  // CTA WhatsApp URL Update
  const cta = document.getElementById('cta-final');
  if (cta) {
    if (lang === 'en') {
      cta.href = "https://wa.me/573004000707?text=Hello%20Goshen%20Hills%2C%20I%20would%20like%20to%20request%20private%20information%20about%20the%20collection%20of%2011%20villas.";
    } else {
      cta.href = "https://wa.me/573004000707?text=Hola%2C%20quiero%20recibir%20informaci%C3%B3n%20privada%20sobre%20Goshen%20Hills.";
    }
  }

  // Sidebar translation refresh
  buildSidebar();
}

function buildSidebar() {
  const navMenu = document.getElementById('navMenu');
  navMenu.innerHTML = '';

  const sections = Array.from(document.querySelectorAll('section[data-bloque]'));
  const blocks = {};

  sections.forEach(sec => {
    const blockName = sec.getAttribute('data-bloque');
    if (!blocks[blockName]) blocks[blockName] = [];
    blocks[blockName].push(sec);
  });

  for (const [blockName, secs] of Object.entries(blocks)) {
    const groupDiv = document.createElement('div');
    groupDiv.className = 'nav-group';

    // Attempting simple English mapping for block names if English is active
    let displayBlockName = blockName;
    const lang = localStorage.getItem('goshenLang');
    if (lang === 'en') {
      displayBlockName = blockName
        .replace('Apertura', 'Opening')
        .replace('Lugar', 'Location')
        .replace('Arquitectura', 'Architecture')
        .replace('La colección', 'The Collection')
        .replace('Conversación', 'Conversation');
    }

    const title = document.createElement('div');
    title.className = 'nav-group__title';
    title.innerText = displayBlockName;
    groupDiv.appendChild(title);

    secs.forEach(sec => {
      const link = document.createElement('a');
      link.className = 'nav-link';
      link.href = `#${sec.id}`;

      let secTitle = sec.getAttribute('data-title');
      if (lang === 'en') {
        secTitle = secTitle
          .replace('Portada', 'Cover')
          .replace('El umbral', 'The Threshold')
          .replace('Razón de ser', "Raison d’être")
          .replace('Vía al Rodadero', 'El Rodadero Road')
          .replace('El umbral compartido', 'Shared Threshold')
          .replace('Tipología posterior', 'Posterior Typology')
          .replace('Tipología frontal', 'Frontal Typology')
          .replace('Índice de villas', 'Villa Index')
          .replace('Proceso de acceso', 'Access Process')
          .replace('Cierre', 'Closing');
      }

      link.innerText = secTitle;
      groupDiv.appendChild(link);
    });

    navMenu.appendChild(groupDiv);
  }

  updateActiveSidebar();
}

function updateActiveSidebar() {
  const sections = document.querySelectorAll('section[id]');
  const links = document.querySelectorAll('.nav-link');

  let currentId = '';
  const scrollY = window.scrollY;

  sections.forEach(sec => {
    const top = sec.offsetTop - 300;
    if (scrollY >= top) {
      currentId = sec.id;
    }
  });

  links.forEach(link => {
    link.classList.remove('active');
    if (link.getAttribute('href') === `#${currentId}`) {
      link.classList.add('active');
    }
  });
}

function populateVillaIndex() {
  const listContainer = document.getElementById('villaListContainer');
  if (!listContainer) return;
  listContainer.innerHTML = '';

  const villas = Array.from(document.querySelectorAll('.passport')).filter(p => p.parentElement.id.includes('villa-'));

  villas.forEach((villa, idx) => {
    const id = villa.parentElement.id;
    const num = String(idx + 1).padStart(2, '0');
    const name = villa.querySelector('h2').innerText;
    const desc = villa.querySelector('.subtitle-italic').innerText;

    const item = document.createElement('a');
    item.className = 'villa-item';
    item.href = `#${id}`;

    item.innerHTML = `
      <div class="villa-item__num">${num}</div>
      <div class="villa-item__name">${name}</div>
      <div class="villa-item__desc">${desc}</div>
      <div class="villa-item__arrow">→</div>
    `;

    listContainer.appendChild(item);
  });
}

// Interactivity Handlers
document.addEventListener('DOMContentLoaded', () => {
  initLanguage();
  populateVillaIndex();

  window.addEventListener('scroll', updateActiveSidebar, { passive: true });

  // Mobile Menu
  const menuToggle = document.getElementById('menuToggle');
  const sidebar = document.getElementById('sidebar');
  if (menuToggle && sidebar) {
    menuToggle.addEventListener('click', () => {
      sidebar.classList.toggle('is-open');
    });

    sidebar.addEventListener('click', (e) => {
      if (e.target.classList.contains('nav-link')) {
        sidebar.classList.remove('is-open');
      }
    });
  }

  // Intersection Observer for Reveal Animations
  const observerOptions = { threshold: 0.15, rootMargin: "0px 0px -50px 0px" };
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('is-visible');
        observer.unobserve(entry.target);
      }
    });
  }, observerOptions);

  document.querySelectorAll('.reveal').forEach(el => observer.observe(el));

  // Lightbox logic
  const lightbox = document.getElementById('lightbox');
  const lbImg = document.getElementById('lightboxImg');
  const lbCap = document.getElementById('lightboxCaption');
  const lbClose = document.getElementById('lightboxClose');

  const openLightbox = (src, alt) => {
    lbImg.src = src;
    lbImg.alt = alt || '';
    lbCap.innerText = alt || '';
    lightbox.classList.add('is-open');
    document.body.style.overflow = 'hidden';
  };

  const closeLightbox = () => {
    lightbox.classList.remove('is-open');
    document.body.style.overflow = '';
    setTimeout(() => { lbImg.src = ''; }, 400);
  };

  document.addEventListener('click', (e) => {
    if (e.target.classList.contains('js-zoomable')) {
      openLightbox(e.target.src, e.target.alt);
    }
  });

  if (lbClose) lbClose.addEventListener('click', closeLightbox);
  if (lightbox) {
    lightbox.addEventListener('click', (e) => {
      if (e.target === lightbox) closeLightbox();
    });
  }
  document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') closeLightbox();
  });
});
